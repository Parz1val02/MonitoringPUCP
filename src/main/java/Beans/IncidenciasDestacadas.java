package Beans;

public class IncidenciasDestacadas {
    private Integer idIncidenciasDestacadas;
    private Integer contadorDestacado;
    private Incidencia incidencias;

    public Integer getIdIncidenciasDestacadas() {
        return idIncidenciasDestacadas;
    }

    public void setIdIncidenciasDestacadas(Integer idIncidenciasDestacadas) {
        this.idIncidenciasDestacadas = idIncidenciasDestacadas;
    }

    public Integer getContadorDestacado() {
        return contadorDestacado;
    }

    public void setContadorDestacado(Integer contadorDestacado) {
        this.contadorDestacado = contadorDestacado;
    }

    public Incidencia getIncidencias() {
        return incidencias;
    }

    public void setIncidencias(Incidencia incidencias) {
        this.incidencias = incidencias;
    }
}
