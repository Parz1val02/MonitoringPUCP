package Beans;

public class MasterTable {
    private String codigoMaster;
    private String nombreMaster;
    private String apellidoMaster;
    private String correoMaster;
    private String dniMaster;

    public String getCodigoMaster() {
        return codigoMaster;
    }

    public void setCodigoMaster(String codigoMaster) {
        this.codigoMaster = codigoMaster;
    }

    public String getNombreMaster() {
        return nombreMaster;
    }

    public void setNombreMaster(String nombreMaster) {
        this.nombreMaster = nombreMaster;
    }

    public String getApellidoMaster() {
        return apellidoMaster;
    }

    public void setApellidoMaster(String apellidoMaster) {
        this.apellidoMaster = apellidoMaster;
    }

    public String getCorreoMaster() {
        return correoMaster;
    }

    public void setCorreoMaster(String correoMaster) {
        this.correoMaster = correoMaster;
    }

    public String getDniMaster() {
        return dniMaster;
    }

    public void setDniMaster(String dniMaster) {
        this.dniMaster = dniMaster;
    }
}
